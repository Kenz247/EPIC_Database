        <h3>Our Approach</h3>  <p>
        The Venn diagram visually represents Wentworth&#8217;s approach to EPIC Learning. The externally-collaborate, interdisciplinary and project based circles overlap. This signifies that some projects fall in only one of those categories; some fall in two categories (the double-shaded areas in the diagram); and some fall in all three categories (the triple-shaded area where all three circles overlap). Wentworth encourages approaches to learning that fall in any part of the diagram. Over time, we intend to move toward projects that are right at the center of the diagram&#8212;that is, they are externally-collaborative, project-based, and interdisciplinary.</p></div>

