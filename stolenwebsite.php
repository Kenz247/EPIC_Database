        <h3>Why EPIC Learning?</h3>
        <p>&#65279;&#65279;Wentworth is striving to be nationally recognized as the university of choice for Externally Collaborative, Project-based, Interdisciplinary Curricula (EPIC) for Learning by 2032. Wentworth&#8217;s strategic planning process determined that &#8220;Wentworth's core purpose and mission is to empower, inspire and innovate through experiential learning&#8221; with the goal of <strong>career success</strong>. In today&#8217;s economy, success depends largely on the ability to work collaboratively with others from different disciplines and organizations. To cultivate those collaborative skills, Wentworth has introduced <strong>EPIC Learning</strong> as an integral part of its undergraduate programs.</p>
<p><a href="http://blogs.wit.edu/provost/">Provost Pinizzotto's October 2014 blog on EPIC Learning</a></p>


                                    </div>
